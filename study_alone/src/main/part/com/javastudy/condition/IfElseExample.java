package main.part.com.javastudy.condition;

//if else문
public class IfElseExample {
    public static void main(String[] args) {
        int score = 85;

        if(score>= 90) {
            System.out.println("점수 90보다 큼");
        } else {
            System.out.println("점수 90보다 작음");
        }
    }
}
